# Beste {{ $userName }},

Het verdelen van keeperbeurten is voor veel jeugdcoaches een terugkerend puzzeltje. Wie staat wanneer in doel? En hoe zorg je dat het eerlijk blijft voor iedereen?

Bij jeugdvoetbalcoach.nl wordt dit automatisch geregeld, maar je houdt als coach altijd zelf de controle over hoe je dit instelt.

### Keeperinstellingen: drie mogelijkheden

Bij het beheren van je spelers kun je aangeven hoe je met keepers om wilt gaan:

**1. Vaste keeper (1 of 2 spelers)**

Deze optie gebruik je door "Keeper" als favoriete positie te kiezen bij je spelers.

- Deze speler(s) staan altijd in doel en worden **nooit** op het veld opgesteld.
- Bij één vaste keeper staat deze alle vier de kwarten in doel.
- Bij twee keepers rouleren zij eerlijk per kwart.

**2. Nieuw: Roulerende keepers**

Deze optie gebruik je door "Wil keepen" aan te vinken als optie bij je spelers.

- Meerdere spelers wisselen het keepen per kwart af.
- Wanneer zij niet in doel staan, spelen ze gewoon op het veld.

**3. Iedereen kan keepen**

Geen vaste keepers en geen voorkeur van spelers die willen keepen? Dan kiest het systeem automatisch per kwart een andere speler. Iedereen komt eerlijk aan bod, verspreid over de wedstrijden.

### Maar wat als...

Het systeem houdt met veel scenario's rekening. Zo kan het voorkomen dat je vaste keeper afwezig is. In dat geval schakelt het systeem automatisch over naar het volgende scenario en wordt er gerouleerd met andere spelers (wil keepen of iedereen).
Je kunt na het genereren van een opstelling altijd handmatige wijzigingen doorvoeren, dus de controle ben je nooit kwijt.

